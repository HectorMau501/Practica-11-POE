    private void jbnPushActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbnPushActionPerformed
        
        
        
        nombre = jtfNombre.getText();
        codigo = Integer.parseInt(jtfCodigo.getText());
        direccion = jtfDireccion.getText();
        telefono = Integer.parseInt(jtfNumero.getText());
        
        listaAlumno.push(nombre, codigo, direccion, telefono);
        jtfNombre.setText("");
        jtfDireccion.setText("");
        jtfCodigo.setText("");
        jtfNumero.setText("");
        jtfNombre.requestFocus();
        
        mensajeDeAlerta("Usuario Registrado" , "Registro" );
        
    }//GEN-LAST:event_jbnPushActionPerformed

